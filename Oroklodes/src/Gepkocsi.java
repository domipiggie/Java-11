public class Gepkocsi {
    private Integer sebesseg, tengelySzam, hengerSzam;

    public Integer getSebesseg() {
        return sebesseg;
    }

    public Integer getTengelySzam() {
        return tengelySzam;
    }

    public Integer getHengerSzam(){
        return hengerSzam;
    }

    public void setSebesseg(Integer sebesseg) {
        this.sebesseg = sebesseg;
    }

    public void setTengelySzam(Integer tengelySzam) {
        this.tengelySzam = tengelySzam;
    }

    public void setHengerSzam(Integer hengerSzam) {
        this.hengerSzam = hengerSzam;
    }
}
